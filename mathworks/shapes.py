class square():
    def __init__(self):
        self._length = self._perimeter = self._area = 0
    def setLength(self, length):
        self._length = length
        self._perimeter = self._area = 0
    def getLength(self):
        return self._length
    def getPerimeter(self):
        if self._perimeter == 0:
            self._perimeter = self._length * 4
        return self._perimeter
    def getArea(self):
        if self._area == 0:
            self._area = self._length * self._length
        return self._area
class rectangle():
    def __init__(self):
        self._length = self._width = self._perimeter = self._area = 0
    def setLength(self, length):
        self._length = length
        self._perimeter = self._area = 0
    def getLength(self):
        return self._length
    def setWidth(self, width):
        self._width = width
        self._perimeter = self._area = 0
    def getWidth(self):
        return self._width
    def getPerimeter(self):
        if self._perimeter == 0:
            self._perimeter = (self._length * 2) + (self._width * 2)
        return self._perimeter
    def getArea(self):
        if self._area == 0:
            self._area = self._length * self._width
        return self._area
class circle:
    def __init__(self):
        try:
            import math
            self._pi = math.pi
        except:
            self._pi = 3.14159265
        self._radius = 0
        self._diameter = 0
        self._circumference = 0
        self._area = 0
    def setRadius(self, radius):
        self._radius = radius
        self._diameter = self._radius * 2
        self._circumference = self._area = 0
    def setDiameter(self, diameter):
        self._diameter = diameter
        self._radius = self._diameter / 2
        self._circumference = self._area = 0
    def getRadius(self):
        return self._radius
    def getDiameter(self):
        return self._diameter
    def getPi(self):
        return self._pi
    def getCircumference(self):
        if self._circumference == 0:
            self._circumference = self._diameter * self._pi
        return self._circumference
    def getArea(self):
        if self._area == 0:
            self._area = self._pi * (self._radius * self._radius)
class triangle():
    # The triangle is by far the most complex shape here.
    def __init__(self):
        self._side = [0, 0, 0]
        self._angle = [0, 0, 0]
        self._perimeter = self._area = self._base = self._height = 0
    def setSide(self, side, length):
        self._side[side] = length
    def getBase(self):
        return self._base
    def getHeight(self):
        return self._height
    def setBase(self, value):
        self._base = value
    def setHeight(self, value):
        self._height = value
    def getSide(self, side):
        return self._side[side-1]
    def getPerimeter(self):
        if self._perimeter == 0:
            self._perimeter = self._side[0] + self._side[1] + self._side[2]
    def getArea(self):
        if self._area == 0:
            self._area = (self._height * self._base) / 2
        return self._area
    def setAngle(self, angle, measure):
        self._angle[angle - 1] = measure
    def getAngle(self, angle):
        if self._angle[angle - 1] == 0:
            angle_map = {1: (1, 2), 2: (0, 2), 3: (0, 1)}
            anglet = self._angle[angle_map[angle][0]] + self._angle[angle_map[1]]
            self._angle[angle - 1] = 180 - anglet
        return self._angle[angle - 1]
